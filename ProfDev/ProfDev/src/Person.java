
public class Person {
	String name;
	private double horor = 1;
	private double action = 4;
	private double comedy = 5;
	public Person(String name) {
		this.name = name;
	}
	public String getName() {
		return name;
	}
	public double getHoror() {
		return horor;
	}
	public double getAction() {
		return action;
	}
	public double getComedy() {
		return comedy;
	}
	
}
